  static AppData ensureReferenceData(AppData data) {
    return data.copyWith(
      films: _mergeById(data.films, _referenceFilms, (item) => item.id),
      chemicals:
          _mergeById(data.chemicals, _referenceChemicals, (item) => item.id),
      recipes: _mergeById(data.recipes, _referenceRecipes, (item) => item.id),
    );
  }

  static List<T> _mergeById<T>(
    List<T> existing,
    List<T> reference,
    String Function(T item) idOf,
  ) {
    final merged = [...existing];
    for (final referenceItem in reference) {
      final index =
          merged.indexWhere((item) => idOf(item) == idOf(referenceItem));
      if (index >= 0) {
        merged[index] = referenceItem;
      } else {
        merged.add(referenceItem);
      }
    }
    return merged;
  }

