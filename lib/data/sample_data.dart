  static DevelopmentRecipe _microphen({
    required String filmId,
    required String brand,
    required String name,
    required int nominalIso,
    required int ei,
    required String dilution,
    required String time,
    SourceType sourceType = SourceType.manufacturer,
    String sourceName =
        'ILFORD powder film developers technical data / Microphen',
    String? sourceDate,
    ConfidenceLevel confidence = ConfidenceLevel.high,
    String notes =
        'Tempo base a 20 C / 68 F; punto di partenza da verificare nel proprio workflow.',
  }) {
    return DevelopmentRecipe(
      id: _recipeId(filmId, ei, dilution, sourceType),
      filmId: filmId,
      filmBrand: brand,
      filmName: name,
      nominalIso: nominalIso,
      exposedIso: ei,
      ei: ei,
      developerId: 'chemical_ilford_microphen',
      developerName: 'ILFORD MICROPHEN',
      dilution: dilution,
      baseTemperatureC: 20,
      baseTimeSeconds: TimeFormatter.parseMinutesSeconds(time),
      agitation: 'Manuale standard secondo datasheet o workflow personale.',
      sourceType: sourceType,
      sourceName: sourceName,
      sourceDate: sourceDate,
      notes: notes,
      confidence: confidence,
    );
  }

  static String _recipeId(
    String filmId,
    int ei,
    String dilution,
    SourceType sourceType,
  ) {
    final sourceSuffix =
        sourceType == SourceType.manufacturer ? '' : '_${sourceType.name}';
    return 'recipe_microphen_${filmId.replaceFirst('film_', '')}_${ei}_${dilution.replaceAll('+', '_')}$sourceSuffix';
  }
